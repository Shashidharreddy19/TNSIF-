package com.tnsif.hierarchical;

public class A {
	    void display1() {
	        System.out.println("This is class A.");
	    }
	}
	class B extends A {
	    void display2() {
	        System.out.println("This is class B.");
	    }
	}
	class C extends A {
	    void display3() {
	        System.out.println("This is class C.");
	    }
	}
	class D extends A {
	    void display4() {
	        System.out.println("This is class D.");
	    }
}
